//Relational Operators

#include <iostream>

using namespace std;

main()
{
    int a=10;
    int b=5;
    cout << (a == b) <<endl; // true (1) when both variables have the same value
    cout << (a != b) <<endl; // False (0) when both variables have the same value
    cout << (a > b) <<endl; // true (1) when a is larger than b
    cout << (a < b) <<endl; // true (1) when a is smaller than b
    cout << (a >= b) <<endl; // true (1) when a is smaller than or equal to b
    cout << (a <= b) <<endl; // true (1) when a is larger than or equal to b
    cout << !(a == b) <<endl; // true (1) when the variables are not equal
}



